export const sportsGrageKor = (value: string) => {
  switch (value) {
    case 'ROOKIE':
      return '루키'
    case 'BEGINNER_1':
      return '비기너 1'
    case 'BEGINNER_2':
      return '비기너 2'
    case 'BEGINNER_3':
      return '비기너 3'
    case 'AMATEUR_1':
      return '아마추어 1'
    case 'AMATEUR_2':
      return '아마추어 2'
    case 'AMATEUR_3':
      return '아마추어 3'
    case 'SEMI_PRO_1':
      return '세미프로 1'
    case 'SEMI_PRO_2':
      return '세미프로 2'
    case 'PRO':
      return '프로'
  }
}
